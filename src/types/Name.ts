/**
 * Generates a random name based on the specified type.
 * @param type - The type of name to generate. Can be 'first', 'last', or 'full'. Defaults to 'full'.
 * @returns A random name based on the specified type.
 */
export default function generateName(type: 'first' | 'last' | 'full' = 'full') {

  const firstName = firstNames[Math.floor(Math.random() * firstNames.length)]
  const lastName = lastNames[Math.floor(Math.random() * lastNames.length)]

  if (type === 'first') {
    return firstName
  } else if (type === 'last') {
    return lastName
  } else {
    return `${firstName} ${lastName}`
  }
}