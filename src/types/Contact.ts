/**
 * Generates a random contact based on the specified type.
 * @param type - The type of contact to generate. Can be 'email', 'phone', 'mobile', 'fax', or 'json'.
 * @returns The generated contact based on the specified type.
 */
export default function generateContact(type: 'email' | 'phone' | 'mobile' | 'fax' | 'json') {

    const email = emails[Math.floor(Math.random() * emails.length)]
    const phone = phones[Math.floor(Math.random() * phones.length)]
    const mobile = mobiles[Math.floor(Math.random() * mobiles.length)]
    const fax = faxs[Math.floor(Math.random() * faxs.length)]
  
    if (type === 'email') {
        return email
    } else if (type === 'phone') {
        return phone
    } else if (type === 'mobile') {
        return mobile
    } else if (type === 'fax') {
        return fax
    } else if (type === 'json') {
        return {
            email,
            phone,
            mobile,
            fax
        }
    }
  }